    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $organizator = Organizator::find($id);
        $organizator->delete();
        return redirect('organizations')->with('message','Deleted!');
